class PersonneRepository extends ServiceEntityRepository
{
    private $manager;

    public function __construct
    (
        ManagerRegistry $registry,
        EntityManagerInterface $manager
    )
    {
        parent::__construct($registry, Personne::class);
        $this->manager = $manager;
    }
    public function savePersonne($nom, $prenom, $email, $password)
    {
        $newPersonne = new Personne();

        $newPersonne
            ->setNom($nom)
            ->setPrenom($prenom)
            ->setLogin($email)
            ->setPassword($password);

        $this->manager->persist($newPersonne);
        $this->manager->flush();
    }

    public function remove(Personne $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function removePersonne(Personne $personne)
              {
    $this->manager->remove($personne);
    $this->manager->flush();
            }

            public function updatePersonne(Personne $personne): Personne
            {
                $this->manager->persist($personne);
                $this->manager->flush();
            
                return $personne;
